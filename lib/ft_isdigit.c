#include "libft.h"

int	ft_isdigit(char *str)
{
	int	i;

	i = -1;
	if (str[0] == '-')
		i++;
	while (str[++i])
	{
		if (!(str[i] >= 48 && str[i] <= 57))
			return (0);
	}
	if (i > 17)
		return (0);
	return (1);
}
